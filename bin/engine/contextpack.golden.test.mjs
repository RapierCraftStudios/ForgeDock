/**
 * forge#2703: golden-fixture regression suite for the context-pack builder.
 *
 * Exercises the FULL mineContext() -> assemblePack() -> validateContextPack()
 * pipeline end-to-end against recorded/hand-constructed fixtures, so a future
 * change to any of the three modules (#2700 schema, #2701 miner, #2702
 * assembler) cannot silently drift the assembled pack's output without a test
 * failing. This is distinct from contextpack.mine.test.mjs, which unit-tests
 * mineContext() in isolation and never runs the pack through
 * assemblePack()/validateContextPack().
 *
 * Each scenario lives under `__fixtures__/contextpack/{name}/`:
 *   - fixture.json       — raw mocked `gh` responses (POST-jq shape — the
 *                          mocked io.gh stands in for `gh` + `--jq` together,
 *                          see forge#2717) plus mineContext()/assemblePack()
 *                          call parameters (issueNumber, repo, phaseId).
 *   - expected-pack.json — the committed, golden `assemblePack()` output.
 *                          Generated by actually running the real pipeline
 *                          once (never hand-typed independently — see
 *                          bin/engine/__fixtures__/contextpack/README.md).
 *
 * Required scenarios (forge#2680 acceptance criteria):
 *   1. empty-issue          — no comments, no labels; pack is minimal but
 *                             schema-valid.
 *   2. truncated-comments   — a comment carries a GitHub truncation marker
 *                             AND is large enough to force assemblePack() to
 *                             truncate the slice; miner does not crash, pack
 *                             flags the truncation.
 *   3. malformed-annotation — one annotation missing its :COMPLETE sentinel,
 *                             one with invalid marker syntax; miner does not
 *                             throw, well-formed sibling annotations still
 *                             parse correctly.
 *   4. deleted-gist         — a FORGE annotation references a gist that
 *                             404s; miner marks it unavailable, pack stays
 *                             schema-valid.
 *   5. satellite-repo       — cross-repo (satellite) issue; every mocked gh
 *                             call carries the satellite repo, pack shape is
 *                             unaffected.
 *   6. paginated-comments   — >100-comment thread split across 2
 *                             `--paginate` pages; miner pages through ALL
 *                             comments, pack still respects the size cap.
 *
 * @license MIT
 */

import { describe, it } from 'node:test';
import assert from 'node:assert/strict';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { mineContext, assemblePack } from './contextpack.mjs';
import { validateContextPack, MAX_SLICE_BYTES } from '../../packages/protocol/src/contextpack-schema.js';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const FIXTURES_DIR = path.join(__dirname, '__fixtures__', 'contextpack');

/** Build a mocked `io` from a fixture's raw `gh` response data. Mirrors the
 * `ioFor({...})` convention in `contextpack.mine.test.mjs`, extended to (a)
 * accept a raw (possibly multi-page) `commentsRaw` string instead of a
 * pre-parsed array — required for the pagination scenario, since real
 * `gh api --paginate --jq` output is one JSON array literal PER PAGE
 * concatenated back-to-back, not one combined array — and (b) capture every
 * call's argv for the satellite-repo scenario's `-R`/`repos/{repo}/...`
 * assertions. */
function ioForFixture(fixture) {
  const calls = [];
  const { issueView, commentsRaw = '[]', timelineRaw = '[]', prListById = {}, gistById = {} } = fixture;
  return {
    calls,
    gh: async (args) => {
      calls.push(args.slice());
      const cmd = args.join(' ');
      if (cmd.startsWith('issue view')) return JSON.stringify(issueView);
      if (cmd.includes('/comments')) return commentsRaw;
      if (cmd.includes('/timeline')) return timelineRaw;
      if (cmd.startsWith('issue list')) {
        const searchIdx = args.indexOf('--search');
        const search = searchIdx >= 0 ? args[searchIdx + 1] : '';
        const prMatch = /^#(\d+)/.exec(search || '');
        const pr = prMatch ? prMatch[1] : null;
        return JSON.stringify(prListById[pr] !== undefined ? prListById[pr] : []);
      }
      if (cmd.startsWith('gist view')) {
        const gistId = args[2];
        const entry = gistById[gistId];
        if (entry && typeof entry === 'object' && entry.error) throw new Error(entry.error);
        return entry !== undefined ? entry : '';
      }
      throw new Error(`unexpected gh call: ${cmd}`);
    },
  };
}

function loadFixture(name) {
  const dir = path.join(FIXTURES_DIR, name);
  const fixture = JSON.parse(fs.readFileSync(path.join(dir, 'fixture.json'), 'utf-8'));
  const expectedPack = JSON.parse(fs.readFileSync(path.join(dir, 'expected-pack.json'), 'utf-8'));
  return { fixture, expectedPack };
}

/** Run the full mine -> assemble -> validate pipeline for one fixture and
 * assert the result matches the committed golden pack. Shared by every
 * scenario below so a drift in the pipeline is caught identically regardless
 * of which scenario exercised it. Returns {minedData, pack, io} so callers
 * can layer scenario-specific assertions on top. */
async function runAndAssertGolden(name) {
  const { fixture, expectedPack } = loadFixture(name);
  const io = ioForFixture(fixture);
  const minedData = await mineContext(fixture.issueNumber, { io, repo: fixture.repo || undefined });
  const pack = assemblePack(fixture.phaseId, minedData, fixture.assembleOpts || {});

  assert.deepEqual(pack, expectedPack, `assembled pack for fixture "${name}" drifted from the committed golden file`);

  if (pack !== null) {
    const validation = validateContextPack(pack);
    assert.equal(validation.valid, true, `golden pack for "${name}" failed schema validation: ${JSON.stringify(validation.errors)}`);
  }

  return { fixture, minedData, pack, io };
}

describe('contextpack golden fixtures — full mine->assemble->validate pipeline', () => {
  it('scenario 1: empty issue — no comments, no labels; pack is minimal but schema-valid', async () => {
    const { minedData, pack } = await runAndAssertGolden('empty-issue');

    assert.equal(minedData.comments.length, 0);
    assert.deepEqual(minedData.annotations, []);
    assert.deepEqual(minedData.affectedFiles, []);
    assert.deepEqual(minedData.linkedPrs, []);
    assert.deepEqual(minedData.gists, []);

    assert.notEqual(pack, null, 'an issue with a title but no other content still produces a minimal, non-null pack');
    assert.equal(pack.slices.length, 1);
    assert.equal(pack.truncated ?? false, false);
  });

  it('scenario 2: compacted/truncated comment thread — miner does not crash, pack flags the truncation', async () => {
    const { minedData, pack } = await runAndAssertGolden('truncated-comments');

    // The comment body itself carries a GitHub-style truncation marker —
    // mineContext() must still parse it as a normal comment (no crash, no
    // special-cased detection needed at the mining layer).
    assert.equal(minedData.comments.length, 1);
    assert.match(minedData.comments[0].body, /GITHUB TRUNCATED THIS COMMENT/);

    // The comment's CONTRACT annotation body is large enough (400 padding
    // rows) that assemblePack() must truncate the assembled slice to stay
    // under MAX_SLICE_BYTES — this is the pack-level "flags the truncation"
    // signal the acceptance criteria describes.
    assert.equal(pack.slices[0].truncated, true);
    const sliceBytes = Buffer.byteLength(pack.slices[0].content, 'utf-8');
    assert.ok(sliceBytes <= MAX_SLICE_BYTES, `truncated slice is ${sliceBytes} bytes, expected <= MAX_SLICE_BYTES (${MAX_SLICE_BYTES})`);
  });

  it('scenario 3: malformed FORGE annotation — miner does not throw, well-formed siblings still parse', async () => {
    const { minedData } = await runAndAssertGolden('malformed-annotation');

    // Comment 1: CONTEXT annotation missing its :COMPLETE sentinel — still
    // parses as a CONTEXT annotation (sentinel absence is a state, not a
    // parse failure).
    const contextAnnotation = minedData.annotations.find((a) => a.type === 'CONTEXT');
    assert.ok(contextAnnotation, 'CONTEXT annotation with no completion sentinel must still be parsed, not dropped');
    assert.equal(contextAnnotation.sentinelState, 'interrupted');

    // Comment 2: unterminated opening tag (`<!-- FORGE:ARCHITECT` with no
    // closing `-->`) does not match annotation syntax at all — parse() must
    // find nothing there rather than throwing or corrupting adjacent parses.
    const architectAnnotation = minedData.annotations.find((a) => a.type === 'ARCHITECT');
    assert.equal(architectAnnotation, undefined, 'an unterminated opening tag must not be recognized as a structural annotation');

    // Comment 3: well-formed CONTRACT annotation must still be correctly
    // parsed despite its malformed siblings (forge#1524/#1594-style
    // structural-anchor guarantee holds).
    const contractAnnotation = minedData.annotations.find((a) => a.type === 'CONTRACT');
    assert.ok(contractAnnotation, 'a well-formed CONTRACT annotation must parse correctly even when sibling comments carry malformed annotations');
  });

  it('scenario 4: deleted gist — mineContext() marks it unavailable, pack stays schema-valid', async () => {
    const { minedData, pack } = await runAndAssertGolden('deleted-gist');

    assert.equal(minedData.gists.length, 1);
    assert.equal(minedData.gists[0].available, false);
    assert.ok(minedData.gists[0].error, 'a 404d gist must report a non-empty error, not silently succeed');
    assert.equal(minedData.gists[0].content, null);

    // A gist reference is not itself pack content (no SLICE_ANNOTATION_TYPES
    // entry maps to KNOWLEDGE_GIST) — the pack is still built from the
    // sibling CONTRACT annotation and must remain schema-valid.
    assert.notEqual(pack, null);
  });

  it('scenario 5: cross-repo (satellite) issue — every gh call carries the satellite repo', async () => {
    const { fixture, io } = await runAndAssertGolden('satellite-repo');

    assert.equal(fixture.repo, 'RapierCraftStudios/forgedock-platform');

    const issueViewCall = io.calls.find((c) => c[0] === 'issue' && c[1] === 'view');
    assert.ok(issueViewCall.includes('-R') && issueViewCall.includes('RapierCraftStudios/forgedock-platform'), 'issue view must carry -R <satellite repo>');

    const commentsCall = io.calls.find((c) => c.join(' ').includes('/comments'));
    assert.ok(commentsCall[1].includes('repos/RapierCraftStudios/forgedock-platform/issues/'), 'the comments api call must embed the satellite repo in its path, not the default repo');

    const timelineCall = io.calls.find((c) => c.join(' ').includes('/timeline'));
    assert.ok(timelineCall[1].includes('repos/RapierCraftStudios/forgedock-platform/issues/'), 'the timeline api call must embed the satellite repo in its path, not the default repo');
  });

  it('scenario 6: >100-comment thread — miner pages through ALL comments, pack respects the size cap', async () => {
    const { minedData, pack } = await runAndAssertGolden('paginated-comments');

    // 60 (page 1) + 45 (page 2, including the CONTRACT-bearing comment) = 105.
    // A miner that only reads the first --paginate page would report 60, not
    // 105 — this is the "no silent truncation at page 1" guarantee.
    assert.equal(minedData.comments.length, 105);
    assert.equal(minedData.meta.commentCount, 105);
    assert.equal(minedData.meta.partialParseFailure, undefined, 'a clean 2-page mock must not report a parse failure');

    const contractAnnotation = minedData.annotations.find((a) => a.type === 'CONTRACT');
    assert.ok(contractAnnotation, 'the CONTRACT annotation buried on page 2 must still be found — proves page 2 was actually read');

    assert.notEqual(pack, null);
    const sliceBytes = Buffer.byteLength(pack.slices[0].content, 'utf-8');
    assert.ok(sliceBytes <= MAX_SLICE_BYTES, `assembled slice is ${sliceBytes} bytes, expected <= MAX_SLICE_BYTES (${MAX_SLICE_BYTES})`);
  });
});

describe('contextpack golden fixtures — fixture/expected-pack pairing sanity', () => {
  const scenarioNames = fs.readdirSync(FIXTURES_DIR).filter((name) => fs.statSync(path.join(FIXTURES_DIR, name)).isDirectory());

  it('every fixture subdirectory has both fixture.json and expected-pack.json', () => {
    assert.ok(scenarioNames.length >= 6, `expected at least 6 fixture scenarios, found ${scenarioNames.length}: ${scenarioNames.join(', ')}`);
    for (const name of scenarioNames) {
      const dir = path.join(FIXTURES_DIR, name);
      assert.ok(fs.existsSync(path.join(dir, 'fixture.json')), `${name}/fixture.json is missing`);
      assert.ok(fs.existsSync(path.join(dir, 'expected-pack.json')), `${name}/expected-pack.json is missing`);
    }
  });

  it('every required scenario name is present', () => {
    const required = ['empty-issue', 'truncated-comments', 'malformed-annotation', 'deleted-gist', 'satellite-repo', 'paginated-comments'];
    for (const name of required) {
      assert.ok(scenarioNames.includes(name), `required scenario "${name}" is missing from ${FIXTURES_DIR}`);
    }
  });
});
