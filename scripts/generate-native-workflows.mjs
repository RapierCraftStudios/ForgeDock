#!/usr/bin/env node

import { mkdir, readdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join, relative, resolve, sep } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const commandsDir = join(root, "commands");
const output = join(root, "native", "packages", "opencode", "src", "forgedock", "workflows.generated.ts");

async function markdownFiles(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const nested = await Promise.all(
    entries.map((entry) => {
      const path = join(dir, entry.name);
      if (entry.isDirectory()) return markdownFiles(path);
      return entry.isFile() && entry.name.endsWith(".md") ? [path] : [];
    }),
  );
  return nested.flat();
}

const workflows = await Promise.all(
  (await markdownFiles(commandsDir)).sort().map(async (path) => {
    const template = (await readFile(path, "utf8")).trim();
    const name = relative(commandsDir, path).split(sep).join("/").replace(/\.md$/, "");
    const heading = template.match(/^#\s+(.+)$/m)?.[1]?.trim();
    return {
      name,
      description: heading || `ForgeDock workflow: ${name}`,
      template,
    };
  }),
);

const source = [
  "// Generated by scripts/generate-native-workflows.mjs. Do not edit.",
  "// commands/**/*.md is the authoritative ForgeDock workflow source.",
  `export const workflows = ${JSON.stringify(workflows, null, 2)} as const`,
  "",
].join("\n");

await mkdir(dirname(output), { recursive: true });
await writeFile(output, source, "utf8");
process.stdout.write(`Generated ${workflows.length} native ForgeDock workflows.\n`);
